		<h2>Sobre o Bike Market</h2>
 		<p>O Bike Market é uma plataforma online que possibilita que qualquer pessoa encontre rapidamente aquilo de que está à procura para a sua bicicleta.</p>
 		<p>O Bike Market nasceu da dificuldade que atualmente os utilizadores de bicicletas têm para conseguirem encontrar rapidamente o material usado que procuram. Existem vários sites, fóruns e grupos de facebook para a compra e venda de material usado relacionado com BMX, BTT e ciclismo. Contudo, a verdade é que o comprador, geralmente, teria de perder inúmeras horas só para conseguir encontrar os anúncios que seriam relevantes para sí. Isto era não só verdadeiramente desgastante para quem procura material, como uma forma altamente ineficiente de divulgar o material para quem vende.</p>
 		<p>Por esse motivo, o Bike Market foi desenvolvido com o objetivo de ser simples, rápido de utilizar e disponibilizar apenas os resultados que são relevantes para o utilizador.</p>
 		<p>Os diversos anúncios são organizados em 72 categorias diferentes, podendo ainda ser implementados parâmetros de pesquisa mais específicos dentro de cada uma destas categorias. Na prática, isto significa que qualquer utilizador consegue, em menos de 5 segundos, encontrar uma lista de anúncios que correspondem àquilo que procura.</p>

		<h2>Missão</h2>
		<ul>
 			<li>Possibilitar a procura de qualquer material, usado ou semi-novo, relacionado com a bicicleta, de uma forma rápida e eficaz.</li> 
 			<li>Permitir que qualquer pessoa consiga iniciar-se na prática de BMX, BTT ou ciclismo com material de qualidade e de forma económica.</li>
 			<li>Permitir que os utilizadores possam rentabilizar as bicicletas ou componentes que já não utilizam, dando-lhes assim uma nova vida e podendo investir esse dinheiro num futuro upgrade para a sua bike atual.</li>
 		</ul>

		<h2>Visão</h2>
		<ul>
 			<li>Ser o primeiro local a ser consultado para procura de material usado ou semi-novo para bicicletas.</li>
			<li>Estabelecer uma relação benéfica tanto para o comprador como para o vendedor (originar um ganho económico/utilidade para ambas as partes).</li>
		</ul>

		<h2>Valores</h2>
		<ul>
			<li>Contribuir para o impulsionamento de uma vida saudável andando de bicicleta.</li>
			<li>Melhorar a experiência que as pessoas têm com a sua bicicleta.</li>
